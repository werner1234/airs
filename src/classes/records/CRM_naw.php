	/*
	 * Veldvalidatie
	 */
	function validate()
	{
    if($this->get("wachtwoord") !="")
    {
      if(strlen($this->get("wachtwoord")) < 6)
        $this->setError("wachtwoord",vt("Moet minimaar 6 tekens lang zijn!"));
    }
    
    $db=new DB();
    $query="SELECT veldnaam,id FROM CRM_eigenVelden WHERE relatiesoort=1 ";
    $db->SQL($query);
    $db->Query();
    while($data=$db->nextRecord())
    {
       if($this->get($data['veldnaam'])==1)
         $relatieType[]=$data['id'];
    }
    foreach($relatieType as $id)
    {
      $query="SELECT veldnaam,extraVeldData FROM CRM_eigenVelden WHERE id='$id'";
      $db->SQL($query);
      $db->Query();
      while($data=$db->nextRecord())
      {
        $verplichteVelden=unserialize($data['extraVeldData']);
       // listarray($verplichteVelden);
        foreach($verplichteVelden as $object=>$velden)
        {
           foreach($velden as $veld)
           {
             if($this->get($veld)=='')
             {
                $this->setError($veld,vt("Moet gevuld zijn."));
             }
           }
         }
        
      }
    }
    
    $fields = array_keys($this->data['fields']);
    foreach($fields as $field)
    {
      if($this->data['fields'][$field]['validate_notEmpty'])
      {
        if($this->get($field)=='')
           $this->setError($field,vt("Mag niet leeg zijn."));
      }
    }
    
		$query  = "SELECT id,portefeuille FROM CRM_naw WHERE portefeuille = '".$this->get("portefeuille")."' ";
		$db->SQL($query);
		$db->Query();
		$data = $db->nextRecord();
		if($db->records() >0 && $this->get("id") <> $data['id'] && $this->get("portefeuille") <> '' )
		{
			$this->setError("portefeuille", vtb("%s is al gekoppeld.", array($this->get("portefeuille"))));
		}    
		$query  = "SELECT id,CRMGebrNaam,email,wachtwoord FROM CRM_naw WHERE CRMGebrNaam = '".$this->get("CRMGebrNaam")."' ";
		$db->SQL($query);
		$db->Query();
		$data = $db->nextRecord();
		if($db->records() >0 && $this->get("id") <> $data['id'] && $this->get("CRMGebrNaam") <> '')
		{		  
		  //$this->setError("CRMGebrNaam",$this->get("CRMGebrNaam")." is al in gebruik.");
      if($this->get("email") <> $data['email'] || $this->get("wachtwoord") <> $data['wachtwoord'] || ($this->get("email")=='' && $this->get("wachtwoord")==''))
      {
      	$this->setError("CRMGebrNaam",vtb("%s is al in gebruik. (en het emailadres (%s) en wachtwoord (%s) komt niet overeen met dit record.)", array($this->get("CRMGebrNaam"), $data['email'], $data['wachtwoord'])));
      }
		}      

		$valid = ($this->error==false)?true:false;
		return $valid;
	}

	/*
	 * Toegangscontrole
	 */
	function checkAccess($type)
	{
		if($_SESSION['usersession']['superuser'])
		  return true;
		else
		{
		  switch ($type)
		  {
		    case "edit":
					if(GetCRMAccess(0))
					{
						if ($_GET['template'] == 'intake' || $_POST['template'] == 'intake')
						{
							return true;
						}
						if ($this->get('debiteur') == 0)
						{
							return true;
						}
						return GetCRMAccess(1);
					}
					return false;
					break;
        case "delete":
          return GetCRMAccess(2);
          break;
        default:
          return false;
          break;
      }
		}
	}

